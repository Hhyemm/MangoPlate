
import Foundation

struct Search: Codable {
    var isSuccess: Bool
    var code: Int
    var message: String
    var result: [SearchInfo]?
}

struct SearchInfo: Codable {
    var id: Int
    var name: String
    var address: String
    var foodCategory: String
    var latitude: Double
    var longitude: Double
    var numReviews: Int
    var ratingsAvg: Double
    var isWishes: Int
    var isVisits: Int
    var imgUrl: String
    var view: Int
    var distance: Double
}

